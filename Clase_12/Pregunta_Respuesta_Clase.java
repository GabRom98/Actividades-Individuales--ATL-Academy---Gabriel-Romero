package Clase_12;

public class Pregunta_Respuesta_Clase {
    private String pregunta;
    private String respuesta;

    public Pregunta_Respuesta_Clase(String pregunta,String respuesta){
        this.pregunta = pregunta;
        this.respuesta = respuesta;
    }

    public String getPregunta() {
        return pregunta;
    }

    public String getRespuesta() {
        return respuesta;
    }

}
